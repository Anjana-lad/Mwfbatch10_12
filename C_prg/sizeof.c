//sizeof operator : which will show the size of each datatype.
#include<stdio.h>
int main(){
    int num;
    float salary;
    char ch;
    double d;
    long marks;
    printf("\n Size of integer =%d",sizeof(num));
    printf("\n Size of  float =%d",sizeof(salary));
    printf("\n Size of character =%d",sizeof(ch));
    printf("\n Size of double =%d",sizeof(d));
    printf("\n Size of long =%d",sizeof(marks));
}